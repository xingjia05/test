<?php
/* 物品个数 */
$N = 4;
$C = [-1, 2, 1, 3];
$W = [-1, 2, 1, 3];
$V = 4;
$F = [];
for ($i = 0; $i <= $N; $i++) {
    for ($j = 0; $j <= $V; $j++) {
        $F[$i][$j] = 0;
    }
}

for ($i=1; $i <= $N; $i++) {
    for ($v=0; $v <= $V; $v++) {
        $F[$i][$v] = $F[$i-1][$v];
        if ($C[$i] <= $v && ($F[$i][$v] < $F[$i-1][$v-$C[$i]] + $W[$i])) {
           $F[$i][$v] = $F[$i-1][$v-$C[$i]] + $W[$i];
        }
    }
}
print_r($F);
print_r($F[$N][$V]);
die;
